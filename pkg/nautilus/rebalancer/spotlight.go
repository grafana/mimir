package rebalancer

import (
	"fmt"
	"math/rand"
	"sync"
	"time"

	"github.com/grafana/mimir/pkg/nautilus/assignment"
)

// Spotlight is a single hash range the rebalancer has flagged for
// fine-grained diagnostic logging by downstream consumers
// (distributors and readcache pods). The rebalancer creates one when
// it commits a move involving the range; the entry lives for a
// fixed duration after creation and is then pruned. The TraceID is
// opaque and lets a single move's downstream effects be grepped out
// across pods.
//
// Named "Spotlight" rather than "SpotlightedRange" so the public
// proto message (SpotlightedRange, generated by service.proto) and
// this internal domain type don't collide.
type Spotlight struct {
	TraceID       string
	Range         assignment.HashRange
	StartedAt     time.Time
	ExpiresAt     time.Time
	FromPartition int32
	ToPartition   int32
	Reason        string
}

// Default sampling/duration constants. Tuned for ~16-20 Phase 3
// moves per round on a 300-partition cluster: at 5% we sample ~1
// move/round; over a 10-minute window that's roughly 20 concurrent
// spotlights at steady state. Both are below the cardinality cap
// downstream consumers want for per-spotlight in-memory bookkeeping.
const (
	defaultSpotlightSampleRate = 0.05
	defaultSpotlightDuration   = 10 * time.Minute
)

// spotlightStore holds the active spotlight set. add/prune mutate
// it; snapshot is a read path consulted by the GetSpotlightedRanges
// gRPC server and by tests. Internally guarded by a sync.RWMutex so
// the read path (which the gRPC server hits on arbitrary client
// goroutines) is concurrent-safe with the single-threaded write
// path (driven by rebalance()).
type spotlightStore struct {
	mu      sync.RWMutex
	entries map[string]Spotlight
	rng     *rand.Rand

	// sampleRate is the per-action sampling probability. Configured
	// via newSpotlightStore so tests can force-sample (rate=1.0).
	sampleRate float64

	// duration is how long after creation a spotlight is kept
	// before prune drops it. Configurable so tests can use a much
	// shorter window than production's 10 minutes.
	duration time.Duration
}

// newSpotlightStore constructs an empty store. The RNG is seeded
// from the supplied source so callers (tests, in particular) can
// inject a deterministic stream of decisions.
func newSpotlightStore(seed int64, sampleRate float64, duration time.Duration) *spotlightStore {
	return &spotlightStore{
		entries:    make(map[string]Spotlight),
		rng:        rand.New(rand.NewSource(seed)),
		sampleRate: sampleRate,
		duration:   duration,
	}
}

// maybeSpotlight rolls the sampling die and, if it comes up, records
// a new spotlight entry for the given range/move and returns it. If
// the roll fails, returns the zero value and false. The caller
// should emit the decision-time log only when ok=true.
//
// Rationale for sampling inside the store rather than at the call
// site: the RNG is store-internal so tests can swap it for a
// deterministic source via newSpotlightStore; keeping the predicate
// here also means add() is a separate (unconditional) entry point
// for tests that want to construct exact spotlight sets.
func (s *spotlightStore) maybeSpotlight(at time.Time, hr assignment.HashRange, fromPID, toPID int32, reason string) (Spotlight, bool) {
	s.mu.Lock()
	defer s.mu.Unlock()
	if s.rng.Float64() >= s.sampleRate {
		return Spotlight{}, false
	}
	e := Spotlight{
		TraceID:       fmt.Sprintf("sp-%d-%d-%d", hr.Lo, hr.Hi, at.UnixNano()),
		Range:         hr,
		StartedAt:     at,
		ExpiresAt:     at.Add(s.duration),
		FromPartition: fromPID,
		ToPartition:   toPID,
		Reason:        reason,
	}
	s.entries[e.TraceID] = e
	return e, true
}

// add records the supplied entry unconditionally. Used by tests; the
// rebalancer's production path uses maybeSpotlight.
func (s *spotlightStore) add(e Spotlight) {
	s.mu.Lock()
	defer s.mu.Unlock()
	s.entries[e.TraceID] = e
}

// prune drops spotlight entries whose ExpiresAt is at or before at.
// Called once per rebalance round; entries that expire between
// prunes are still returned by snapshot() until the next prune, but
// since the duration is much larger than the round interval the
// extra lag is acceptable.
func (s *spotlightStore) prune(at time.Time) {
	s.mu.Lock()
	defer s.mu.Unlock()
	for id, e := range s.entries {
		if !at.Before(e.ExpiresAt) {
			delete(s.entries, id)
		}
	}
}

// snapshot returns a defensive copy of all active spotlight entries.
// Order is not specified — callers that need a stable order should
// sort by TraceID.
func (s *spotlightStore) snapshot() []Spotlight {
	s.mu.RLock()
	defer s.mu.RUnlock()
	out := make([]Spotlight, 0, len(s.entries))
	for _, e := range s.entries {
		out = append(out, e)
	}
	return out
}

// len returns the active spotlight count. Used in tests; production
// callers use snapshot() or the gRPC method.
func (s *spotlightStore) len() int {
	s.mu.RLock()
	defer s.mu.RUnlock()
	return len(s.entries)
}
