func TestRenderPackage(t *testing.T) {
	pkg := parseFixture(t, `package core
                     import "github.com/grafana/mimir/pkg/streamingpromql/planning"
                     //node:generate
                     type Subquery struct {
                     	Inner planning.Node `+"`"+`node:"child"`+"`"+`
                     }
                     //node:generate
                     type FunctionCall struct {
                     	Args []planning.Node `+"`"+`node:"children,labelfmt=param %d"`+"`"+`
                     }`)

	out, err := RenderGenerators(pkg, CreateGenerators(), "node_gen.go")
	require.NoError(t, err)
	got := string(out)

	// Whatever the generators emit, the file as a whole must be valid Go.
	_, err = parser.ParseFile(token.NewFileSet(), "gen.go", got, 0)
	require.NoError(t, err, "generated source must parse as valid Go")

	// File-level scaffolding.
	require.Contains(t, got, "// Code generated by node-gen. DO NOT EDIT.")
	require.Contains(t, got, "package core")
	require.Contains(t, got, "import")
	require.Contains(t, got, "github.com/grafana/mimir/pkg/streamingpromql/planning")

	require.Contains(t, got, "*Subquery")
	require.Contains(t, got, "*FunctionCall")
}

// parseFixture parses a single Go source string into a Package, failing the test if parsing fails.
func parseFixture(t *testing.T, source string) *Package {
	t.Helper()
	fset := token.NewFileSet()
	f, err := parser.ParseFile(fset, "fixture.go", source, parser.ParseComments)
	require.NoError(t, err)
	pkg, err := buildPackage([]*ast.File{f})
	require.NoError(t, err)
	return pkg
}

func gofmt(t *testing.T, src string) string {
	t.Helper()
	formatted, err := formatSource(src, "expected.go", true)
	require.NoError(t, err, "expected source to be valid Go: %s", src)
	return strings.TrimRight(string(formatted), "\n")
}

func resolveAnnotatedStruct(t *testing.T, pkg *Package) *Struct {
	t.Helper()
	require.Len(t, pkg.NodeStructs, 1, "fixture must declare exactly one //node:generate struct")
	return pkg.NodeStructs[0]
}
