#
# Regenerates the cqa.5 wiresmith proto cluster (standalone protos):
#   - pkg/ingester/client/ingester.proto      (imports mimir.proto; gRPC streaming service)
#   - pkg/distributor/distributorpb/distributor.proto (imports mimir.proto; gRPC service)
#   - pkg/usagetracker/usagetrackerpb/usagetracker.proto (plain gRPC service; no cross-module imports)
#   - pkg/storage/indexheader/indexheaderpb/sparse.proto (plain messages; no cross-module imports)
#
# ingester.proto and distributor.proto import mimir.proto by Go module path, so
# we stage a temporary tree mirroring the module-path layout and run wiresmith
# against it (same recipe as cqa.2/cqa.3).
#
# usagetracker.proto and sparse.proto have no cross-module imports beyond
# wiresmith/options.proto (which wiresmith bundles), so they are compiled
# with a simple --proto_path pointing at their own directories.

set -eu -o pipefail

MODULE=github.com/grafana/mimir
P=${MODULE}/pkg
STAGE=.cqa5-stage
OUT=.cqa5-out

# Cluster protos that are emitted (relative to the repo root).
# usagetrackerpb and indexheaderpb are compiled separately below (no staging needed).
EMIT_PROTOS=(
	pkg/ingester/client/ingester.proto
	pkg/distributor/distributorpb/distributor.proto
)

# Imported-but-not-emitted wiresmith protos (staged for cross-file reference resolution).
IMPORT_PROTOS=(
	pkg/mimirpb/mimir.proto
)

rm -rf "${STAGE}" "${OUT}"

stage_proto() {
	local rel="$1"
	local dest="${STAGE}/${P}/${rel#pkg/}"
	mkdir -p "$(dirname "${dest}")"
	cp "${rel}" "${dest}"
}

for proto in "${EMIT_PROTOS[@]}"; do
	stage_proto "${proto}"
done

for proto in "${IMPORT_PROTOS[@]}"; do
	stage_proto "${proto}"
done

# Build -M flags for all staged protos (emitted + imported).
M_FLAGS=()
for proto in "${EMIT_PROTOS[@]}"; do
	M_FLAGS+=(-M "${P}/${proto#pkg/}=${P}/$(dirname "${proto#pkg/}")")
done
for proto in "${IMPORT_PROTOS[@]}"; do
	M_FLAGS+=(-M "${P}/${proto#pkg/}=${P}/$(dirname "${proto#pkg/}")")
done

# Positional emit args (paths under STAGE).
EMIT_ARGS=()
for proto in "${EMIT_PROTOS[@]}"; do
	EMIT_ARGS+=("./${STAGE}/${P}/${proto#pkg/}")
done

mkdir -p "${OUT}"
~/go/bin/wiresmith --proto_path="./${STAGE}" --out="./${OUT}" --module="${MODULE}" \
	"${M_FLAGS[@]}" \
	"${EMIT_ARGS[@]}"

# Copy generated files back into the real package directories.
copy_back() {
	local src_dir="$1"
	local dst_dir="$2"
	for sfx in .pb.go _compare.pb.go _util.pb.go _grpc.pb.go; do
		for f in "${OUT}/${src_dir}"/*"${sfx}"; do
			# The glob may expand to the literal pattern when no files match;
			# test -f guards against that without triggering set -e.
			if [ -f "${f}" ]; then
				cp "${f}" "${dst_dir}/"
			fi
		done
	done
}

copy_back "${P}/ingester/client" "pkg/ingester/client"
copy_back "${P}/distributor/distributorpb" "pkg/distributor/distributorpb"

# distributor.proto is a pure-service proto with no message types of its own.
# Wiresmith emits only distributor_grpc.pb.go + distributor_util.pb.go; the
# distributor.pb.go that gogo used to emit contained only gRPC stubs (now in
# distributor_grpc.pb.go) and must be replaced with a minimal stub so that make's
# grouped &: rule (which lists distributor.pb.go as a target) sees the file exist
# and the generic %.pb.go: %.proto gogo rule doesn't fire.
cat >"pkg/distributor/distributorpb/distributor.pb.go" <<'EOF'
// SPDX-License-Identifier: AGPL-3.0-only
// Provenance-includes-location: https://github.com/cortexproject/cortex/blob/master/pkg/distributor/distributorpb/distributor.proto
// Provenance-includes-license: Apache-2.0
// Provenance-includes-copyright: The Cortex Authors.

// Code generated by wiresmith. DO NOT EDIT.
// source: github.com/grafana/mimir/pkg/distributor/distributorpb/distributor.proto

// distributor.proto defines only a gRPC service (no message types of its own).
// All generated code lives in distributor_grpc.pb.go and distributor_util.pb.go.

package distributorpb
EOF

rm -rf "${STAGE}" "${OUT}"

# Compile usagetrackerpb and indexheaderpb with simple proto_path
# (no cross-module imports; wiresmith bundles wiresmith/options.proto).
STAGE2=.cqa5b-stage
OUT2=.cqa5b-out

rm -rf "${STAGE2}" "${OUT2}"
mkdir -p "${STAGE2}/${P}/usagetracker/usagetrackerpb"
cp pkg/usagetracker/usagetrackerpb/usagetracker.proto \
	"${STAGE2}/${P}/usagetracker/usagetrackerpb/"

mkdir -p "${OUT2}"
~/go/bin/wiresmith \
	--proto_path="./${STAGE2}" \
	--out="./${OUT2}" \
	--module="${MODULE}" \
	-M "${P}/usagetracker/usagetrackerpb/usagetracker.proto=${P}/usagetracker/usagetrackerpb" \
	"./${STAGE2}/${P}/usagetracker/usagetrackerpb/usagetracker.proto"

copy_back() {
	local src_dir="$1"
	local dst_dir="$2"
	for sfx in .pb.go _compare.pb.go _util.pb.go _grpc.pb.go; do
		for f in "${OUT2}/${src_dir}"/*"${sfx}"; do
			if [ -f "${f}" ]; then
				cp "${f}" "${dst_dir}/"
			fi
		done
	done
}

copy_back "${P}/usagetracker/usagetrackerpb" "pkg/usagetracker/usagetrackerpb"

rm -rf "${STAGE2}" "${OUT2}"

# Compile indexheaderpb.
STAGE3=.cqa5c-stage
OUT3=.cqa5c-out

rm -rf "${STAGE3}" "${OUT3}"
mkdir -p "${STAGE3}/${P}/storage/indexheader/indexheaderpb"
cp pkg/storage/indexheader/indexheaderpb/sparse.proto \
	"${STAGE3}/${P}/storage/indexheader/indexheaderpb/"

mkdir -p "${OUT3}"
~/go/bin/wiresmith \
	--proto_path="./${STAGE3}" \
	--out="./${OUT3}" \
	--module="${MODULE}" \
	-M "${P}/storage/indexheader/indexheaderpb/sparse.proto=${P}/storage/indexheader/indexheaderpb" \
	"./${STAGE3}/${P}/storage/indexheader/indexheaderpb/sparse.proto"

copy_back() {
	local src_dir="$1"
	local dst_dir="$2"
	for sfx in .pb.go _compare.pb.go _util.pb.go _grpc.pb.go; do
		for f in "${OUT3}/${src_dir}"/*"${sfx}"; do
			if [ -f "${f}" ]; then
				cp "${f}" "${dst_dir}/"
			fi
		done
	done
}

copy_back "${P}/storage/indexheader/indexheaderpb" "pkg/storage/indexheader/indexheaderpb"

rm -rf "${STAGE3}" "${OUT3}"
